a = int(input("Enter Your Number: "))
b = int(input("Enter Your Number: "))
op = input("Enter Your Operater: ")
if (op=='+'):
    print(a + b)
elif(op=='-'):
    print(a - b)
elif(op=='*'):
    print(a * b)
elif(op=='/'):
    print(a / b)
else:
    print("Enter a Valid Operator")
    
